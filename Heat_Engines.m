% and their performance is expressed in terms of the thermal efficiency
% eta_th, which is the ratio of the net work produced by the engine to the
% total heat input. 
% External combustion engines --> Heat is supplied to the working fluid
% from an external source such as a furnace , a geothermal well, a nuclear
% reactor , or even the sun.
% Internal combustion engines --> Heat is supplied to the working fluid by burning the fuel within the system
